         zexp=(0.,-0.666666666666667)*rootz_cubed
     &       +(0., 1.308996938995747)

         if ( REAL(zz) .lt. 0.) then

            exp1=EXP(zexp)
c           exp2=EXP(zexp-i*pi*4/3)
            exp2=EXP(zexp+(0.,-4.188790207))

            if (AIMAG(zz) .ge. 0.) then

               zh2 =zh2 *exp1
               zh2p=zh2p*exp1

               if (ngboth .eq. 0) then

                  zh2 =zh2 +zh1 /exp2
                  zh2p=zh2p+zh1p/exp2
               end if

               zh1 =zh1 /exp1
               zh1p=zh1p/exp1
            else

               th2 =-zh1 /exp2
               th2p=-zh1p/exp2

               zh1 =zh1 /exp1+zh2 *exp2
               zh1p=zh1p/exp1+zh2p*exp2

               if (ngboth .eq. 0) then

                  zh2 =zh2 *exp1
                  zh2p=zh2p*exp1
               else

                  zh2 =th2
                  zh2p=th2p
               end if
            end if
            zexp=(0.,0.)
         end if

         zterm=alpha/SQRT(rootz)
         zh1  =zterm*zh1
         zh2  =zterm*zh2
         zh1p =zterm*zh1p
         zh2p =zterm*zh2p
      end if
